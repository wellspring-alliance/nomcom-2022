<h1>Positions</h1>

<p>
  The Nominating Committee is seeking to identify nominees for the following
  positions in 2022.
</p>

<h3>Elder</h3>
<p>
  Elders provide wisdom and spiritual direction to the Governing Board work of
  providing overall guidance for the church. As a group, elders serve as
  pastoral advisors, oversee membership and lead in discipline and restoration.
  Per the CMA constitution, elders must be male.
</p>

<h3>Member at Large</h3>
<p>
  Members at Large share in church governance and oversight responsibilities of
  the Governing Board, including vision setting, planning, and decision making
  on church-wide policy and business issues.
</p>

<h3>Treasurer</h3>
<p>
  The Treasurer chairs the Finance Committee, disburses funds in accordance
  with the approved budget, oversees accounting staff, and participates on the
  Governing Board in vision setting, oversight, policy development and business
  decisions for all the churchâ€™s ministries.
</p>

<h3>Financial Secretary</h3>
<p>
  The Financial Secretary receives all donations on behalf of the church,
  keeping records, and documenting the source and purpose for all donations.
  They participate on the Governing Board in vision setting, oversight, and
  decision making on policy and business issues.
</p>

<h3>Deacon</h3>
<p>
  Deacons lead the congregation in meeting the financial, emotional and
  physical needs of people within the church family or to people in our
  community with whom the church has contact. At their meetings, deacons make
  decisions about meeting specific financial needs and in between meetings,
  individual deacons take steps to minister to people in need.
</p>

<h3>Deacon Chair</h3>
<p>
  The Deacon Chair leads the Deacon Committee in ministry of meeting needs
  and caring for people in crisis.
</p>


